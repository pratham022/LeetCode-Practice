class Solution
{
public:
    
    void helper(Node* root, int ctr, int sum, int &ansCtr, int &ansSum) {
        if(root== NULL) 
            return;
            
        ctr++;
        sum += root->data;
        
        if(root->left==NULL && root->right==NULL) {
            if(ctr >= ansCtr) {
                ansCtr = ctr;
                ansSum = max(ansSum, sum);
            }
        }
        else {
            helper(root->left, ctr, sum, ansCtr, ansSum);
            helper(root->right, ctr, sum, ansCtr, ansSum);
        }
    }
    int sumOfLongRootToLeafPath(Node *root)
    {
        //code here
        int sum = 0;
        int ansSum = 0;
        
        int ctr = 0;
        int ansCtr = 0;
        
        helper(root, ctr, sum, ansCtr, ansSum);
        return ansSum;
    }
};

